package com.mani.pradeep.tricky;

public class ShortExample {

    public static void main(String[] args) {
        short s1 = 5;
        short s2 = 7;
        returnShort((short) (s1 + s2));
    }
    public static short returnShort(short s) {
        System.out.println(s);
        return s;
    }
}
